#include "libft.h"
#include <stdio.h>

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = ft_strlen(s);
	while (i >= 0 && s[i] != (unsigned char)c)
		i--;
	return ((s[i] == (unsigned char)c) ? (char *)&s[i] : NULL);
}
